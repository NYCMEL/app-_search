app.init = function() {
    console.group("app.init");
    
    console.group("app.init");
    
    $('body').attr('id','wc');

    $(".wc-header-bot-menus li a").on("click", function(e) {
	let el = $(this);
	let id = el.attr("id");

	$(".apage").hide(0, function() {
	    $("#page-" + id).show(0);
	});
    });

    console.groupEnd();
};

